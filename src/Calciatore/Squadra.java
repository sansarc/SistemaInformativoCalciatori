package Calciatore;

public class Squadra {
    private String nome, nazionalita, categoria;

    public void setNome(String s) {nome = s;}
    public void setNazionalita(String s) {nazionalita = s;}
    public void setCategoria(String s) {categoria = s;}

    public String getNome() {return nome;}
    public String getNazionalita() {return nazionalita;}
    public String getCategoria() {return categoria;}
}
